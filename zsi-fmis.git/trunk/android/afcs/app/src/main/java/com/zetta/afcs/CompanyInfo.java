package com.zetta.afcs;

public class CompanyInfo {
    public String Name;
    public String Tin;

    public CompanyInfo() {}

    public void setName(String name) {
        this.Name = name;
    }

    public String getName() {
        return this.Name;
    }

    public void setTin(String tin) {
        this.Tin = tin;
    }

    public String getTin() {
        return this.Tin;
    }
}
